import UIKit

extension UIColor {
    
    class func notifiGray() -> UIColor {
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1)
    }
    class func notifiTeal() -> UIColor {
        return UIColor(red: 23/255, green: 210/255, blue: 217/255, alpha: 1)
    }
    class func notifiDarkTeal() -> UIColor {
        return UIColor(red: 14/255, green: 134/255, blue: 139/255, alpha: 1)
    }
    class func noticeYellow() -> UIColor{
        return UIColor(red: 253/255, green: 245/255, blue: 144/255, alpha: 1)
    }
    class func noticeGreen() -> UIColor{
        return UIColor(red: 35/255, green: 164/255, blue: 72/255, alpha: 1)
    }
    class func noticeRed() ->UIColor{
        return UIColor(red: 183/255, green: 30/255, blue: 17/255, alpha: 1)
    }
    class func noticeButtonYellow() -> UIColor{
        return UIColor(red: 254/255,green: 222/255, blue: 50/255, alpha: 1)
    }
    class func noticeButtonGreen() -> UIColor{
        return UIColor(red: 35/255,green: 164/255, blue: 72/255, alpha: 1)
    }
    class func noticeButtonRed() -> UIColor{
        return UIColor(red: 251/255,green: 53/255, blue: 51/255, alpha: 1)
    }
    class func noticeGrey() ->UIColor{
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1)
    }
    class func noticeButtonGrey() ->UIColor{
        return UIColor(red: 155/255, green: 155/255, blue: 155/255, alpha: 1)
    }
    class func nightlyBackgroundGrey() ->UIColor{
        return UIColor(red: 233/255, green: 233/255, blue: 233/255, alpha: 1)
    }
}
