import UIKit

extension UIColor {
    
    class func notifiGray() -> UIColor {
        return UIColor(red: 74/255, green: 74/255, blue: 74/255, alpha: 1)
    }
    class func notifiGreen() -> UIColor {
        return UIColor(red: 87/255, green: 184/255, blue: 189/255, alpha: 1)
    }
    class func notifiDarkGreen() -> UIColor {
        return UIColor(red: 58/255, green: 126/255, blue: 131/255, alpha: 1)
    }
    
}
