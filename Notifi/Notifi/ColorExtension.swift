import UIKit

extension UIColor {
    
    class func notifiGray() -> UIColor {
        return UIColor(red: 67/255, green: 67/255, blue: 67/255, alpha: 1)
    }
    class func notifiWhite() -> UIColor {
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1)
    }
    
}
